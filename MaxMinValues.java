public class MaxMinValues {
    public static void main(String[] args) {
        int maxInt = Integer.MAX_VALUE;
        int minInt = Integer.MIN_VALUE;

        double maxDouble = Double.MAX_VALUE;
        double minDouble = Double.MIN_VALUE;

        int maxChar = Character.MAX_VALUE;
        int minChar = Character.MIN_VALUE;

        System.out.println("Máximo int: " + maxInt);
        System.out.println("Mínimo int: " + minInt);
        System.out.println("Máximo double: " + maxDouble);
        System.out.println("Mínimo double: " + minDouble);
        System.out.println("Máximo char: " + maxChar);
        System.out.println("Mínimo char: " + minChar);
    }
}
